/**
 *  @mainpage stemkit-cpp - All you need to start off a C++ project.
 *
 *  This library provides utility classes to use in a C++ project. Its design
 *  was inspired by standard Java class library, especialy collection
 *  framework (@java_api_link{util}) and streams (@java_api_link{io}).
 *  
 *  The functionality is split into the following namespaces:
 *
 *  <dl>
 *    <dt>sk::util
 *    <dd>
 *      Provides collection framework. Plus utility classes for basic types.
 *
 *    <dt>sk::io
 *    <dd>
 *      Provides stream classes.
 *
 *    <dt>sk::rt
 *    <dd>
 *      Provides runtime support. This includes:
 *        - Scope centered logging and configuration 
 *        - Config files lookup and XML parsing
 *        - Threading and synchronization
 *  </dl>
*/
